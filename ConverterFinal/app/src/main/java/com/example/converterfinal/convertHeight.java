package com.example.converterfinal;

public class convertHeight {
    public class convertHeight {

        public static double mTOcm(double m) {
            double cm;
            cm = m * 100;
            return cm;
        }

        public static double cmTom(double cm) {
            double m;
            m = cm / 100;
            return m;
        }

        public static double mTOmm(double m) {
            double mm;
            mm = m * 1000;
            return mm;
        }
    }
}
